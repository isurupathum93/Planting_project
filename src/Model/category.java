package Model;

/**
 *
 * @author Isuru Pathum
 */

    public class category {

        private String categoryid;
        private String categoryname;
        private String categoryCode;
        private String categoryDesc;
        private String categoryStatus;
        private String categoryAdjust;

        /**
         * @return the categoryid
         */
        public String getCategoryid() {
            return categoryid;
        }

        /**
         * @param categoryid the categoryid to set
         */
        public void setCategoryid(String categoryid) {
            this.categoryid = categoryid;
        }

        /**
         * @return the categoryname
         */
        public String getCategoryname() {
            return categoryname;
        }

        /**
         * @param categoryname the categoryname to set
         */
        public void setCategoryname(String categoryname) {
            this.categoryname = categoryname;
        }

        /**
         * @return the categoryCode
         */
        public String getCategoryCode() {
            return categoryCode;
        }

        /**
         * @param categoryCode the categoryCode to set
         */
        public void setCategoryCode(String categoryCode) {
            this.categoryCode = categoryCode;
        }

        /**
         * @return the categoryDesc
         */
        public String getCategoryDesc() {
            return categoryDesc;
        }

        /**
         * @param categoryDesc the categoryDesc to set
         */
        public void setCategoryDesc(String categoryDesc) {
            this.categoryDesc = categoryDesc;
        }

        /**
         * @return the categoryStatus
         */
        public String getCategoryStatus() {
            return categoryStatus;
        }

        /**
         * @param categoryStatus the categoryStatus to set
         */
        public void setCategoryStatus(String categoryStatus) {
            this.categoryStatus = categoryStatus;
        }

        /**
         * @return the categoryAdjust
         */
        public String getCategoryAdjust() {
            return categoryAdjust;
        }

        /**
         * @param categoryAdjust the categoryAdjust to set
         */
        public void setCategoryAdjust(String categoryAdjust) {
            this.categoryAdjust = categoryAdjust;
        }
    }

